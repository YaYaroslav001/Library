    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        String student_id = LibraryCardField.getText();
        String name = NameField.getText();
        String address = AddressField.getText();
        String phone = PhoneNumberField.getText();
        int id = Integer.parseInt(student_id);
        try
        {
            java.sql.Connection con = ConnectionProvider.getCon();
            Statement stmt = con.createStatement();
            stmt.executeUpdate("delete from mydb.student where library_card_no = "+id+" and student_name = '"+name+"' and address = '"+address+"' and phone_number = '"+phone+"'");
            JOptionPane.showMessageDialog(null, "Студент успешно удалён!");
            main.RefreshStudent();
            setVisible(false);
        }
        catch(Exception e)
        {
            System.err.println(e);
            JOptionPane.showMessageDialog(null, "Введены некорректные данные о студенте!");
            setVisible(false);
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

