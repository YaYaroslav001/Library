    private void EnterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnterButtonActionPerformed
        String student_id = LibraryCardField.getText();
        String name = NameField.getText();
        String address = AddressField.getText();
        String phone = PhoneNumberField.getText();
        int id = Integer.parseInt(student_id);
        try
        {
            java.sql.Connection con = ConnectionProvider.getCon();
            Statement stmt = con.createStatement();
            stmt.executeUpdate("update mydb.student set student_name = '"+name+"', address = '"+address+"', phone_number = '"+phone+"' where library_card_no = "+id+"");
            JOptionPane.showMessageDialog(null, "Студент успешно отредактирован!");
            main.RefreshStudent();
            setVisible(false);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Что-то пошло не так!");
            setVisible(false);
        }
    }//GEN-LAST:event_EnterButtonActionPerformed

